    void Depositar() {
        
        String monto = txtDeposito.getText();
        Integer amount;
        amount = Integer.valueOf(monto);
        String sql= "UPDATE tcuentasol SET salCueSol=salCueSol+"+amount+" WHERE idCueSol="+idCs+";";

        
        if (monto.equals("")){
        JOptionPane.showMessageDialog(null,"Falta ingresar el monto!");

        } else {
            try {

                if (amount >= 10 && amount<=10000 && (amount%10==0)) {
                    conet = con1.getConnection();
                    st = conet.createStatement();
                    st.executeUpdate(sql);

                    JOptionPane.showMessageDialog(null, "Introduzca los billetes y presione OK");

                    RegistrarDepositoS();
                    V_saldo_soles s = new V_saldo_soles();
                    s.setVisible(true);
                    this.dispose();                    
                } else {
                    JOptionPane.showMessageDialog(null, "Monto no válido. Intente de nuevo");
                    V_final v = new V_final();
                    v.setVisible(true);
                    this.dispose();
                }

        } catch (Exception e) {
            System.err.println("No se puedo establecer conexión con la base de datos. Error: " + e);
            JOptionPane.showMessageDialog(this, "Error, intente de nuevo más tarde");
        
        }
       }
    }
    
